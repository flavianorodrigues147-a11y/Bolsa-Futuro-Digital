# area do quadrado e volume do quadrado
l = float(input("Digite o valor do lado do quadrado: "))
area = l * l  # lado vezes lado
print("A área do quadrado é:", area)
# volume do quadrado
l = float(input("Digite o valor do lado do quadrado para calcular o volume: "))
volume = l ** 3  # cumprimento do lado elevado ao cubo
print("O volume do quadrado é:", volume)
