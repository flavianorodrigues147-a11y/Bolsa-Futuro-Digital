# Cria uma lista de 5 nomes
nomes_lista = ['Ana', 'Bruno', 'Carlos', 'Diana', 'Eduardo']

# Converte a lista em tupla
nomes_tupla = tuple(nomes_lista)

# Exibe os dois tipos
print("Lista:", nomes_lista)
print("Tipo da lista:", type(nomes_lista))
print("Tupla:", nomes_tupla)
print("Tipo da tupla:", type(nomes_tupla))
